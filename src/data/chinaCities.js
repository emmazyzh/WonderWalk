export const CHINA_PROVINCES = Object.entries(CHINA_PROVINCE_CITIES).map(([provinceCode, value]) => ({
  code: provinceCode,
  name: value.provinceName,
}))

export function getProvinceCities(provinceCode) {
  const province = CHINA_PROVINCE_CITIES[provinceCode]
  if (!province) return []

  return province.cities.map((city, index) => ({
    code: `${provinceCode}-${index + 1}`,
    provinceCode,
    provinceName: province.provinceName,
    name: city,
    nameEn: city,
  }))
}

export function getAllChinaCities() {
  return Object.keys(CHINA_PROVINCE_CITIES).flatMap((provinceCode) => getProvinceCities(provinceCode))
}
